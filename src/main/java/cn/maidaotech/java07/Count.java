package cn.maidaotech.java07;
public class Count {
        
  public static void main(String[] args) {
   Double x=7.0;
   Double y=6.0;
   System.out.println(x+"和"+y+"的相乘的结果是:"+Number.ride(x, y));
   System.out.println(x+"和"+y+"的相除的结果是:"+Number.divide(x, y));
   System.out.println(x+"和"+y+"的相加的结果是:"+Number.plus(x, y));
   System.out.println(x+"和"+y+"的相减的结果是:"+Number.minus(x, y));
  }
    
}